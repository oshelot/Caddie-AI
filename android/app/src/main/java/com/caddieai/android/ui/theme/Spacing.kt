package com.caddieai.android.ui.theme

import androidx.compose.ui.unit.dp

object Spacing {
    /** Tight grouping within a component (iOS: 4pt) */
    val xs = 4.dp
    /** Between related components (iOS: 8pt) */
    val sm = 8.dp
    /** Inside cards and form fields (iOS: 12pt) */
    val md = 12.dp
    /** Card-level padding, list item padding (iOS: 16pt) */
    val lg = 16.dp
    /** Between major sections (iOS: 20pt) */
    val xl = 20.dp
    /** Top-level screen sections (iOS: 24pt) */
    val xxl = 24.dp
}